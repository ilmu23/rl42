#include "internal/_term.h"
#include "internal/_utils.h"

u8	backward_char(rl42_line *line) {
	if (line->i) {
		line->i--;
		return term_cursor_move_to(line, line->root.row, line->root.col + calculate_cursor_offset(line));
	}
	return 1;
}
